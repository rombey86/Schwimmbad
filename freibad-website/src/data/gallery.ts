export const getImagesByCategory = (category: GalleryImage['category']) => {
  return galleryImages.filter(image => image.category === category);
};

export const getAllCategories = (): GalleryImage['category'][] => {
  return ['allgemein', 'bauphase', 'aktionstag', 'flyer'];
};

export const getCategoryLabel = (category: GalleryImage['category']): string => {
  switch (category) {
    case 'allgemein':
      return 'Allgemein';
    case 'bauphase':
      return 'Bauphase';
    case 'aktionstag':
      return 'Aktionstage';
    case 'flyer':
      return 'Flyer & Plakate';
    default:
      return 'Unbekannt';
  }
};

export const getCategoryColor = (category: GalleryImage['category']): string => {
  switch (category) {
    case 'allgemein':
      return 'blue';
    case 'bauphase':
      return 'orange';
    case 'aktionstag':
      return 'green';
    case 'flyer':
      return 'purple';
    default:
      return 'gray';
  }
};
